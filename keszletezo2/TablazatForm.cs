        private void betoltesBtn_Click(object sender, EventArgs e)
        {
            progressBar1.Visible = true;
            Api proxy = apiHivas();
            for (int i = 0; i < 942; i++)
            {
                string inventoryId = inventory[i];
                var inv = proxy.ProductInventoryFind(inventoryId);
                var prod = proxy.ProductsFindAll();
                if (inv.Content.QuantityOnHand < 5)
                {

                    //adat példányosítása
                    keszletezo.Model.Adat adat = new keszletezo.Model.Adat();
                    adat.bvin = inventory[i];
                    adat.ProductBvin = inv.Content.ProductBvin;
                    adat.VariantId = inv.Content.VariantId;
                    adat.QuantityOnHand = inv.Content.QuantityOnHand;
                    adat.QuantityReserved = inv.Content.QuantityReserved;
                    adat.QuantityAvailableForSale = inv.Content.QuantityOnHand; //ezt nem lehet lekérni
                    adat.LowStockPoint = inv.Content.LowStockPoint;
                    adat.LastUpdated = inv.Content.LastUpdated;
                    adat.StoreId = i; //ezt sem lehet lekérni
                    adat.OutOfStockPoint = inv.Content.OutOfStockPoint;
                    for (int j = 0; j < 942; j++)
                    {
                        if (prod.Content[j].Bvin == inv.Content.ProductBvin)
                        {
                            adat.ProductName = prod.Content[j].ProductName;
                            break;
                        }
                    }

                    inventoryData.Add(adat);

                }

                int progress = Convert.ToInt16((i + 1) * 100 / 942);
                progressBar1.Value = progress;

            }
        }
    }
